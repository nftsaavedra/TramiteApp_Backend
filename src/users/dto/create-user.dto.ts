import {
  IsEmail,
  IsEnum,
  IsNotEmpty,
  IsOptional,
  IsString,
  IsUUID,
  MinLength,
} from 'class-validator';
import { Role } from '@prisma/client';

export class CreateUserDto {
  @IsEmail()
  @IsNotEmpty()
  email: string;

  @IsString()
  @IsNotEmpty()
  name: string;

  @IsString()
  @IsNotEmpty()
  @MinLength(8, { message: 'La contrase√±a debe tener al menos 8 caracteres' })
  password: string;

  @IsEnum(Role)
  @IsNotEmpty()
  role: Role;

  @IsUUID()
  @IsOptional()
  oficinaId?: string | null;
}
